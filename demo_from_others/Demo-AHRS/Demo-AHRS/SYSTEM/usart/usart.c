//串口1发送一个字符
void UsartSend(uint8_t ch)
{
	USART_SendData(USART1, (uint8_t) ch);
	while (USART_GetFlagStatus(USART1, USART_FLAG_TC) == RESET);
}


//传送数据到匿名四轴上位机
void uart1_niming_report(u8 fun,u8 *data,u8 len)
{
	u8 send_buf[32];
	u8 i;
	if(len>28)return;	//最多28字节数据 
	send_buf[len+3]=0;	//校验数置零
	send_buf[0]=0X88;	//帧头
	send_buf[1]=fun;	//功能字
	send_buf[2]=len;	//数据长度
	for(i=0;i<len;i++)send_buf[3+i]=data[i];			//复制数据
	for(i=0;i<len+3;i++)send_buf[len+3]+=send_buf[i];	//计算校验和	
	for(i=0;i<len+4;i++)UsartSend(send_buf[i]);	//发送数据到串口1 
}


//发送加速度传感器数据 陀螺仪数据 磁力计数据
void mpu9250_send_data(short aacx,short aacy,short aacz,short gyrox,short gyroy,short gyroz,short magx,short magy,short magz)
{
	u8 tbuf[18]; 
	tbuf[0]=(aacx>>8)&0XFF;//分离出高8位 
	tbuf[1]=aacx&0XFF;//低8位
	tbuf[2]=(aacy>>8)&0XFF;
	tbuf[3]=aacy&0XFF;
	tbuf[4]=(aacz>>8)&0XFF;
	tbuf[5]=aacz&0XFF; 
	tbuf[6]=(gyrox>>8)&0XFF;
	tbuf[7]=gyrox&0XFF;
	tbuf[8]=(gyroy>>8)&0XFF;
	tbuf[9]=gyroy&0XFF;
	tbuf[10]=(gyroz>>8)&0XFF;
	tbuf[11]=gyroz&0XFF;
	tbuf[12]=(magx>>8)&0XFF;
	tbuf[13]=magx&0XFF;
	tbuf[14]=(magy>>8)&0XFF;
	tbuf[15]=magy&0XFF;
	tbuf[16]=(magz>>8)&0XFF;
	tbuf[17]=magz&0XFF;
	uart1_niming_report(0XA1,tbuf,18);//自定义帧,0XA1
}

